package Twenty_eighth_package;

public class Student {
    int potionMaking;
    int historyOfMagic;
    int herbology;
    int transfiguration;

    public Student(int potionMaking, int historyOfMagic, int herbology, int transfiguration) {
        this.potionMaking = potionMaking;
        this.historyOfMagic = historyOfMagic;
        this.herbology = herbology;
        this.transfiguration = transfiguration;
    }
}
